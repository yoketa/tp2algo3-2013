package fiuba.algo3.gpschallenge.modelo;

public class Vector {

	private int x;
	private int y;

	public Vector(int x,int y){
		this.setX(x);
		this.setY(y);
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
}
